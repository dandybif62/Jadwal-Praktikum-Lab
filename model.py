class Mahasiswa:
    def __init__(self, nim, nama):
        self.nim = nim
        self.nama = nama

class Ruangan:
    def __init__(self, nama, kapasitas):
        self.nama = nama
        self.kapasitas = kapasitas

class Praktikum:
    def __init__(self, nama, hari, mulai, selesai):
        self.nama = nama
        self.hari = hari
        self.mulai = mulai
        self.selesai = selesai
